def step_function(x):
    return np.array(x > 0, dtype=np.int)


def sigmoid(x):
    return 1 / (1 + np.exp(-x))    

    
def relu(x):
    return np.maximum(0, x)

    
def softmax(a):
    c = np.max(a)
    exp_a = np.exp(a - c)
    sum_exp_a = np.sum(exp_a)
    y = exp_a / sum_exp_a

    return y


def mean_squard_error(y, t):
    return 0.5 * np.sum((y - t) ** 2)


